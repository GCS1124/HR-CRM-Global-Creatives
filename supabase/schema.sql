create or replace function public.ensure_profile()
returns public.profiles
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  v_user_id uuid := auth.uid();
  v_email text;
  v_full_name text;
  v_profile public.profiles;
begin
  if v_user_id is null then
    raise exception 'No authenticated user found.';
  end if;

  select *
  into v_profile
  from public.profiles
  where id = v_user_id;

  if found then
    return v_profile;
  end if;

  select
    u.email,
    coalesce(
      nullif(btrim(u.raw_user_meta_data ->> 'full_name'), ''),
      nullif(btrim(u.raw_user_meta_data ->> 'name'), ''),
      split_part(u.email, '@', 1)
    )
  into v_email, v_full_name
  from auth.users u
  where u.id = v_user_id;

  insert into public.profiles (id, email, full_name, role)
  values (
    v_user_id,
    v_email,
    v_full_name,
    case
      when lower(v_email) = any(public.admin_emails()) then 'admin'
      else 'employee'
    end
  )
  on conflict (id) do update
    set email = excluded.email,
        full_name = coalesce(excluded.full_name, public.profiles.full_name)
  returning * into v_profile;

  update public.employees
  set user_id = v_user_id
  where lower(email) = lower(v_email)
    and user_id is null;

  return v_profile;
end;
$$;

create or replace function public.handle_new_auth_user_compat()
returns trigger
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  v_email text := lower(coalesce(new.email, ''));
  v_full_name text := coalesce(
    nullif(btrim(new.raw_user_meta_data ->> 'full_name'), ''),
    nullif(btrim(new.raw_user_meta_data ->> 'name'), ''),
    nullif(split_part(coalesce(new.email, ''), '@', 1), ''),
    'User'
  );
  v_role text := case
    when lower(coalesce(new.raw_user_meta_data ->> 'role', 'employee')) = 'admin' then 'admin'
    when v_email = any(public.admin_emails()) then 'admin'
    else 'employee'
  end;
begin
  begin
    insert into public.profiles (id, email, full_name, role)
    values (
      new.id,
      new.email,
      v_full_name,
      v_role
    )
    on conflict (id) do update
      set email = excluded.email,
          full_name = coalesce(excluded.full_name, public.profiles.full_name),
          role = excluded.role;
  exception
    when unique_violation then
      null;
  end;

  update public.employees
  set user_id = new.id
  where lower(email) = v_email
    and user_id is null;

  return new;
end;
$$;

